
    <h1>Tokyo</h1>
    <p>Tokyo is the capital city of Japan. It is the most populous city in the country, with a metropolitan area of over 37 million inhabitants, making it one of the largest urban areas in the world.</p>
    <p>Located on the eastern coast of Honshu, Tokyo has grown from a small fishing village named Edo into a global metropolis. It became the capital in 1868 when Emperor Meiji moved the imperial seat there, renaming it Tokyo, meaning "Eastern Capital."</p>
