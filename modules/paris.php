    <h1>Paris</h1>
    <p>Paris is the capital city of France. It is the most populous city in the country, with a metropolitan area of over 12 million inhabitants.</p>
    <p>Situated on the River Seine, Paris has been a major settlement for centuries, with its history dating back to ancient times when it was founded by a Celtic tribe known as the Parisii, and later developed by the Romans, who called it Lutetia.</p>
